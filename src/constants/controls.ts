// ============================================================================
// TYPES
// ============================================================================

export type FrameworkId = 'SOC2' | 'ISO27001' | 'HIPAA' | 'NIST' | 'PCIDSS' | 'GDPR';

export type ControlStatus = 'not_started' | 'in_progress' | 'implemented' | 'not_applicable';

export type ComplianceDomain = 
  | 'access_control'
  | 'asset_management'
  | 'risk_assessment'
  | 'security_operations'
  | 'incident_response'
  | 'business_continuity'
  | 'vendor_management'
  | 'data_protection'
  | 'physical_security'
  | 'hr_security'
  | 'change_management'
  | 'compliance_monitoring';

export interface FrameworkMapping {
  frameworkId: FrameworkId;
  clauseId: string;
  clauseTitle: string;
}

export type EffortLevel = 'low' | 'medium' | 'high';
export type ImpactLevel = 'low' | 'medium' | 'high';

export interface MasterControl {
  id: string;
  domain: ComplianceDomain;
  title: string;
  description: string;
  question: string;
  guidance: string;
  whyItMatters?: string;
  evidenceExamples: string[];
  remediationTip: string;
  riskLevel: 'critical' | 'high' | 'medium' | 'low';
  effort?: EffortLevel;
  impact?: ImpactLevel;
  frameworkMappings: FrameworkMapping[];
  keywords: string[];
}

export interface CustomControl {
  id: string;
  title: string;
  description: string;
  question: string;
  category: ComplianceDomain;
  frameworkMappings: FrameworkMapping[];
  effort: EffortLevel;
  impact: ImpactLevel;
  createdAt: string;
  createdBy: string;
}

export interface UserResponse {
  controlId: string;
  answer: 'yes' | 'no' | 'partial' | 'na' | null;
  notes: string;
  evidenceUrls: string[];
  evidenceNotes: string;
  answeredAt: string | null;
}

export interface EvidenceItem {
  id: string;
  controlId: string;
  controlTitle: string;
  notes: string;
  fileNames: string[];
  uploadedAt: string;
}

export interface ComplianceDomainMeta {
  id: ComplianceDomain;
  title: string;
  description: string;
  icon: string;
  color: string;
}

export interface FrameworkMeta {
  id: FrameworkId;
  name: string;
  fullName: string;
  color: string;
  icon: string;
}

